public class JC_03_Literals
{
    public static void main(String[] args) {
        System.out.println("This is about a literal datatypes and variables");

        int a=23;
        float b=3.53f;  //It is a example of leteral variable the value defined to a vairale have "f" at the end which show the value is float without understanding it as double
        double c=3.4542;  //The decimal numbers are considered as default double number so while declaring the decimal number of float use "f" at the end
        short d=546;
        byte e=32;
        char f='p';
        String g="pavan";
        boolean h=true;

        System.out.println("int ="+a);
        System.out.println("float ="+b);
        System.out.println("double ="+c);
        System.out.println("short ="+d);
        System.out.println("byte ="+e);
        System.out.println("char ="+f);
        System.out.println("String ="+g);
        System.out.println("boolean ="+h);


    }
}
