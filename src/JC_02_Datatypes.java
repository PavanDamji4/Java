public class JC_02_Datatypes {
    public static void main(String[] args) {
        System.out.println("This is about Variables and Datatypes");

        int a=34;  //int refers to the integer numbers and int is a datatype which stores the value 34
        double b=3.533;  //double is datatype it stores the decimal numbers
        char m='P';  //char is datatype it stores single characters
        byte s=122;  //byte is a datatype it stores numbers

        System.out.println("a = "+a);
        System.out.println("float ="+b);
        System.out.println("char = "+m);
        System.out.println("byet = " +s);


    }
}
